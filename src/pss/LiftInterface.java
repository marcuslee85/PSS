    private void RandomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RandomButtonActionPerformed
        // Randomise the queues according to requirements
        //5% VVIP cannot share lifts due to vvip protocol
        //10% Black ops cannot share lifts but lock down mode
        
        Random r = new Random();

        if (WeekendToggle.isSelected()) {
            BlackOpsQueue.setValue(r.nextInt(10));
            VVIPQueue.setValue(r.nextInt(1));
            ManagementQueue.setValue(r.nextInt(5));
            BlackOpsEmployeeQueue.setValue(r.nextInt(10));
            EmployeeQueue.setValue(r.nextInt(20));
            VisitorQueue.setValue(r.nextInt(1));
            MaintenanceQueue.setValue(r.nextInt(100));
            CleanerQueue.setValue(r.nextInt(50) + 50);
        } else {
            BlackOpsQueue.setValue(r.nextInt(10000));
            VVIPQueue.setValue(r.nextInt(10000));
            ManagementQueue.setValue(r.nextInt(10000));
            BlackOpsEmployeeQueue.setValue(r.nextInt(10000));
            EmployeeQueue.setValue(r.nextInt(10000));
            VisitorQueue.setValue(r.nextInt(10000));
            MaintenanceQueue.setValue(r.nextInt(10000));
            CleanerQueue.setValue(r.nextInt(10000));
        }

        BlackOpsQueue.setString(String.valueOf(BlackOpsQueue.getValue()));
        VVIPQueue.setString(String.valueOf(VVIPQueue.getValue()));
        ManagementQueue.setString(String.valueOf(ManagementQueue.getValue()));
        BlackOpsEmployeeQueue.setString(String.valueOf(BlackOpsEmployeeQueue.getValue()));
        EmployeeQueue.setString(String.valueOf(EmployeeQueue.getValue()));
        VisitorQueue.setString(String.valueOf(VisitorQueue.getValue()));
        MaintenanceQueue.setString(String.valueOf(MaintenanceQueue.getValue()));
        CleanerQueue.setString(String.valueOf(CleanerQueue.getValue()));

        for (int i = 0; i < BlackOpsQueue.getValue(); i++) {
            queue.add(new Person(1));
        }
        for (int i = 0; i < VVIPQueue.getValue(); i++) {
            queue.add(new Person(2));
        }
        for (int i = 0; i < ManagementQueue.getValue(); i++) {
            queue.add(new Person(3));
        }
        for (int i = 0; i < BlackOpsEmployeeQueue.getValue(); i++) {
            queue.add(new Person(4));
        }
        for (int i = 0; i < EmployeeQueue.getValue(); i++) {
            queue.add(new Person(5));
        }
        for (int i = 0; i < VisitorQueue.getValue(); i++) {
            queue.add(new Person(6));
        }
        for (int i = 0; i < MaintenanceQueue.getValue(); i++) {
            queue.add(new Person(7));
        }
        for (int i = 0; i < CleanerQueue.getValue(); i++) {
            queue.add(new Person(8));
        }
        ProcessingStatus.setText("");

        int i = 0;
        for (Person person : queue) {
            if (person.getTiming() == i) {
                ProcessingStatus.setText(ProcessingStatus.getText() + person.getTypeName() + " " + person.getTiming() + " \r\n");
            }
            i++;
        }
        ProcessingStatus.setText(ProcessingStatus.getText() + "Total in queue: " + queue.size() + " \r\n");

    }//GEN-LAST:event_RandomButtonActionPerformed

